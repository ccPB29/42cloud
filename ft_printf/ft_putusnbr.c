#include "ft_printf.h"

int	ft_putusnbr(unsigned int n)
{
	char	c;
	int		i;

	i = 0;
	if (n >= 10)
	{
		i += ft_putusnbr(n / 10);
	}
	c = (n % 10) + '0';
	i += write(1, &c, 1);
	return (i);
}
